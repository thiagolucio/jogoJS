let nome = 'Primeiro';
let snome = 'Segundo';
let completo = nome + snome;
document.write(completo.length + '<br>'); //pular linha usando html dentro do js
document.write(completo.charAt(3) + '<br>'); // mostrar a letra da posição 3 da variavel completo
document.write(completo.substring(0,4) + '<br>'); //mostrar a variavel completo da primeira posicao até a quarta
document.write(completo.toLowerCase() + '<br>');//converter a string da variavel completo em letras minusculas
document.write(completo.toUpperCase() + '<br>');//converter a string da variavel completo em letras maiúsculas
